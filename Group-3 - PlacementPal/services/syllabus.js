export function getSyllabus(track) {
  switch (track) {
    case 'Programming & DSA': return DSA_SYLLABUS;
    case 'Data Science & ML': return ML_SYLLABUS;
    case 'Database Management & SQL': return SQL_SYLLABUS;
    case 'Backend / Web Dev': return BACKEND_SYLLABUS;
    default: return DSA_SYLLABUS;
  }
}

export function getAllTopics(track) {
  const syllabus = getSyllabus(track);
  return syllabus.flatMap(w => w.topics);
}
